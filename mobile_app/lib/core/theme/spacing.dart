/// Design system spacing tokens for the DeskLine application.
/// Provides consistent spacing values across all components.
class AppSpacing {
  AppSpacing._();

  // Component spacing scale
  static const double xxs = 4.0;
  static const double xs = 8.0;
  static const double sm = 12.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;

  // Section padding
  static const double sectionPadding = 120.0;
  static const double sectionPaddingMobile = 24.0;

  // Card padding
  static const double cardPadding = 16.0;

  // List item spacing
  static const double listItemSpacing = 12.0;
}
